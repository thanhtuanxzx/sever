                        <h3>Abstract</h3>
                        <div class="article-abstract">
                            <p>Recent studies have shown that low-cost multispectral sensors have attracted much interest in developing agricultural applications. This study evaluated the potential of using a low-cost multispectral sensor to predict the sweetness of mango fruit with high export values. A few spectral data preprocessing and wavelength selection algorithms were applied to develop an accurate prediction model. Experimental results showed that unprocessed spectral data of fourteen wavelengths selected by the “regression coefficients” algorithm were suitable for developing a partial least square regression model with a correlation coefficient of 0.703 and a root mean square error of 1.439 °Brix. These results were comparable to recent studies using the same multispectral sensor, confirming the potential use of low-cost multispectral sensors in developing applications and portable devices for fruit quality assessment.</p>
                        </div>
                        <span><strong>Keywords:</strong> Low-cost, nondestructive assessment, multispectral sensor</span>    
